
interface NameDictionary {
    male: string[];
    female: string[];
}


export class NameGenerator {
    private maleNames: string[];
    private femaleNames: string[];

    constructor() {
        this.maleNames = names.male;
        this.femaleNames = names.female;
    }

    // 随机生成一个男性名字
    generateMaleName(): string {
        const index = Math.floor(Math.random() * this.maleNames.length);
        return this.maleNames[index];
    }

    // 随机生成一个女性名字
    generateFemaleName(): string {
        const index = Math.floor(Math.random() * this.femaleNames.length);
        return this.femaleNames[index];
    }

    // 随机生成一个名字（可以指定性别）
    generateName(gender: 'male' | 'female'): string {
        if (gender === 'male') {
            return this.generateMaleName();
        } else if (gender === 'female') {
            return this.generateFemaleName();
        }
        throw new Error('Invalid gender specified');
    }
}
